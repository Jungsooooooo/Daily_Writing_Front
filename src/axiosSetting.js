import Vue from "vue";
import axios from "axios";

axios.defaults.baseURL = "https://localhost:5000";

axios.interceptors.request.use(
  (config) => {
    return config;
  },
  (error) => {
    return Promise.reject(error);
  }
);

axios.interceptors.response.use(
  (response) => {
    return response;
  },
  (error) => {
    return Promise.reject(error);
  }
);

Vue.prototype.$axios = axios;
